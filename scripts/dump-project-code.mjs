/**
 * Gộp toàn bộ mã nguồn dự án vào một file Markdown.
 *
 * Mặc định bỏ qua:
 * - node_modules, .next, .git...
 * - Các file môi trường (.env)
 * - Các file binary/media
 *
 * Chạy:
 *   npm run dump:project
 * hoặc
 *   node scripts/dump-project-code.mjs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.join(__dirname, "..");
const outFile = path.join(projectRoot, "PROJECT_CODE_DUMP.md");

// Bổ sung thêm các thư mục của IDE (.vscode, .idea)
const skipDirs = new Set([
  "node_modules",
  ".next",
  ".git",
  ".turbo",
  ".vercel",
  ".vscode",
  ".idea",
  "dist",
  "build",
  "coverage",
]);

// Bổ sung các file môi trường (.env) để tránh lộ Secret Keys
const skipFiles = new Set([
  "pnpm-lock.yaml",
  "package-lock.json",
  "yarn.lock",
  "bun.lockb",
  "PROJECT_CODE_DUMP.md",
  ".env",
  ".env.local",
  ".env.development",
  ".env.production",
  ".DS_Store",
  "chalo-description.md",
  "chalo-route.md",
]);

const skipExt = new Set([
  ".png", ".jpg", ".jpeg", ".gif", ".webp", ".ico", ".svg",
  ".woff", ".woff2", ".ttf", ".eot",
  ".mp3", ".mp4", ".mov",
  ".zip", ".gz", ".tar",
  ".pdf",
]);

// Sử dụng Object Map để lấy định dạng ngôn ngữ sạch sẽ hơn
const extToLang = {
  ".tsx": "tsx",
  ".ts": "ts",
  ".jsx": "jsx",
  ".js": "js",
  ".mjs": "js",
  ".cjs": "js",
  ".css": "css",
  ".scss": "scss",
  ".json": "json",
  ".md": "md",
  ".html": "html",
  ".yml": "yaml",
  ".yaml": "yaml",
  ".sh": "bash",
};

function walk(dir, acc = []) {
  if (!fs.existsSync(dir)) {
    console.error("Khong tim thay thu muc:", dir);
    process.exit(1);
  }

  for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
    const fullPath = path.join(dir, e.name);

    if (e.isDirectory()) {
      if (skipDirs.has(e.name)) continue;
      walk(fullPath, acc);
      continue;
    }

    if (skipFiles.has(e.name)) continue;
    if (skipExt.has(path.extname(e.name).toLowerCase())) continue;

    acc.push(fullPath);
  }

  return acc;
}

const files = walk(projectRoot).sort((a, b) => a.localeCompare(b));

// Sử dụng WriteStream để ghi file theo luồng (Tránh lỗi tràn RAM với project lớn)
const stream = fs.createWriteStream(outFile, "utf8");

stream.write("# chalo-fe - project code snapshot\n\n");
stream.write(`_Generated: ${new Date().toISOString()}_\n\n`);
stream.write("_Auto-generated by `npm run dump:project`._\n\n");

for (const filePath of files) {
  const relPath = path.relative(projectRoot, filePath).replace(/\\/g, "/");
  const ext = path.extname(filePath).toLowerCase();

  // Lấy ngôn ngữ từ Map, nếu không có trả về chuỗi rỗng
  const lang = extToLang[ext] || "";

  const content = fs.readFileSync(filePath, "utf8");

  // Tách riêng từng dòng write để tránh lỗi cú pháp do ngắt dòng ẩn
  stream.write(`<!-- file: ${relPath} -->\n\n`);
  stream.write("```" + lang + "\n");
  stream.write(content);
  stream.write("\n```\n\n");
}
// Đóng luồng ghi
stream.end();

stream.on("finish", () => {
  console.log(`Da tao ${outFile} (${files.length} file).`);
});